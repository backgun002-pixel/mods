    public static void register() {
        HudRenderCallback.EVENT.register((g, dt) -> render(g));
    }

    private static void render(GuiGraphics g) {
        Minecraft mc = Minecraft.getInstance();
        if (mc.player == null || mc.options.hideGui) return;
        if (SkillClientCache.skill1Name == null) return;

        int sw = mc.getWindow().getGuiScaledWidth();
        int sh = mc.getWindow().getGuiScaledHeight();

        // 슬롯 3개: 핫바 왼쪽 옆에 세로로 배치
        // 핫바 왼쪽 끝 x = sw/2 - 91
        int baseX = sw/2 - 91 - 20;
        int baseY = sh - 52; // 슬롯1이 제일 위

        if (baseX < 0) return;

        boolean archer = "ARCHER".equals(SkillClientCache.currentJob);
        int[] pix1 = archer ? PIX_DOUBLESHOT : PIX_CHARGE;
        int[] pix2 = archer ? PIX_ARROWRAIN  : PIX_BLADESTORM;
        int[] pix3 = archer ? PIX_SHARPEYES  : PIX_BERSERK;

        drawSlot(g, mc, baseX, baseY,      pix1, cd1, maxCd1, false);
        drawSlot(g, mc, baseX, baseY + 18, pix2, cd2, maxCd2, SkillClientCache.skill2Locked);
        drawSlot(g, mc, baseX, baseY + 36, pix3, cd3, maxCd3, SkillClientCache.skill3Locked);

        // 슬롯 번호 키 힌트 (작게)
        g.drawString(mc.font, "R", baseX+5, baseY+18-9, 0xAAAAAA, false);
        g.drawString(mc.font, "F", baseX+5, baseY+18+9, 0xAAAAAA, false);
        g.drawString(mc.font, "G", baseX+5, baseY+18+27, 0xAAAAAA, false);

        // 화면 이펙트 렌더
        renderScreenEffect(g, mc, sw, sh);
    }

    private static void drawSlot(GuiGraphics g, Minecraft mc, int x, int y, int[] pix, int cd, int maxCd, boolean locked) {
        // 슬롯 배경
        g.fill(x, y, x+16, y+16, 0xCC111111);
        // 테두리
        g.fill(x,   y,   x+16, y+1,  0xFF666666);
        g.fill(x,   y,   x+1,  y+16, 0xFF666666);
        g.fill(x,   y+15, x+16, y+16, 0xFF222222);
        g.fill(x+15, y,  x+16, y+16, 0xFF222222);

        if (locked) {
            // 잠금 아이콘
            for (int py = 0; py < 16; py++) {
                for (int px = 0; px < 16; px++) {
                    int c = PIX_LOCK[py*16+px];
                    int a = (c>>24)&0xFF;
                    if (a > 10) g.fill(x+px, y+py, x+px+1, y+py+1, c);
                }
            }
            // 잠금 텍스트 오버레이
            g.fill(x, y, x+16, y+16, 0x88000000);
            return;
        }

        // 아이콘 픽셀 그리기
        for (int py = 0; py < 16; py++) {
            for (int px = 0; px < 16; px++) {
                int c = pix[py*16+px];
                int a = (c>>24)&0xFF;
                if (a > 10) g.fill(x+px, y+py, x+px+1, y+py+1, c);
            }
        }

        // 쿨타임 오버레이
        if (cd > 0 && maxCd > 0) {
            int h = Math.round(16f * cd / maxCd);
            g.fill(x, y, x+16, y+h, 0x99000000);
            g.drawString(mc.font, String.valueOf((cd/20)+1), x+3, y+4, 0xFFFFFF, true);
        }
    }

    private static void renderScreenEffect(GuiGraphics g, Minecraft mc, int sw, int sh) {
        int effect = SkillClientCache.activeScreenEffect;
        if (effect == SkillScreenEffectPayload.EFFECT_NONE) return;

        // 화면 가장자리 색상 오버레이 (vignette 스타일)
        int color;
        if (effect == SkillScreenEffectPayload.EFFECT_BERSERK) {
            color = 0x55FF2200; // 반투명 빨간색
        } else { // SHARPEYES
            color = 0x4400CC44; // 반투명 초록색
        }

        int thickness = 12;
        // 위
        g.fill(0,        0,         sw, thickness,  color);
        // 아래
        g.fill(0,        sh-thickness, sw, sh,      color);
        // 왼쪽
        g.fill(0,        thickness, thickness, sh-thickness, color);
        // 오른쪽
        g.fill(sw-thickness, thickness, sw, sh-thickness, color);

        // 그라데이션 (약간 더 진하게)
        int colorInner = effect == SkillScreenEffectPayload.EFFECT_BERSERK
            ? 0x33FF2200 : 0x2200CC44;
        int t2 = thickness * 2;
        g.fill(0,          thickness,      sw, t2,             colorInner);
        g.fill(0,          sh-t2,          sw, sh-thickness,   colorInner);
        g.fill(thickness,  t2,             t2, sh-t2,          colorInner);
        g.fill(sw-t2,      t2,             sw-thickness, sh-t2, colorInner);
    }
